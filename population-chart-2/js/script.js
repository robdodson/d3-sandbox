var data = _.map(json, function(entry) {
        var result = {};
        result.year = new Date(parseInt( entry.Year, 10 ), 1, 0);
        result.population = parseInt( entry.Population.replace( /,/g, '' ), 10 );
        return result;
    });

var margin = {top: 40, right: 40, bottom: 40, left:100},
    width = 960,
    height = 500;

var x = d3.time.scale()
    .domain([data[0].year, d3.time.year.offset(data[data.length - 1].year, 1)])
    .range([0, width - margin.left - margin.right]);

var y = d3.scale.linear()
    .domain([d3.min(data, function(d) { return d.population; }), d3.max(data, function(d) { return d.population; })]).nice()
    .range([height - margin.top - margin.bottom, 0]);

var xAxis = d3.svg.axis()
    .scale(x)
    .orient('bottom')
    .ticks(d3.time.years, 10)
    .tickPadding(8)
    .tickSize(6);

var yAxis = d3.svg.axis()
    .scale(y)
    .orient('left')
    .tickPadding(8);

var svg = d3.select('body').append('svg')
    .attr('class', 'chart')
    .attr('width', width)
    .attr('height', height)
  .append('g')
    .attr('transform', 'translate(' + margin.left + ', ' + margin.top + ')');

svg.selectAll('.chart')
    .data(data)
  .enter().append('rect')
    .attr('class', 'bar')
    .attr('x', function(d, i) { return x(d.year) - .5; })
    .attr('y', function(d) { return height - margin.top - margin.bottom - (height - margin.top - margin.bottom - y(d.population)) })
    .attr('width', 3)
    .attr('height', function(d) { return height - margin.top - margin.bottom - y(d.population) });

svg.append('g')
    .attr('class', 'x axis')
    .attr('transform', 'translate(0, ' + (height - margin.top - margin.bottom) + ')')
    .call(xAxis);

svg.append('g')
    .attr('class', 'y axis')
    .call(yAxis);